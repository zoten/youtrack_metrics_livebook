defmodule Youtrack do
  @moduledoc """
  YouTrack API client and utilities for Livebook notebooks.

  This package provides:

    * `Youtrack.Client` - API client for fetching issues and activities
    * `Youtrack.Fields` - Helpers for extracting field values from issues
    * `Youtrack.Workstreams` - Workstream matching based on slugs and tags
    * `Youtrack.WorkstreamsLoader` - YAML configuration loader for workstreams
  """
end
